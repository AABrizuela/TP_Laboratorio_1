

/** \brief Funcion suma.
 *
 * \param Primer operando.
 * \param Segundo operando.
 * \return El resultado de la suma entre los 2 operandos.
 *
 */
float fSum(float firstNumber, float secondNumber);

/** \brief Funcion division
 *
 * \param Primer operando.
 * \param Segundo operando.
 * \return El resultado de la division entre los 2 operandos.
 *
 */
float fDivision(float firstNumber, float secondNumber);

/** \brief
 *
 * \param Primer operando.
 * \param Segundo operando.
 * \return El resultado de la multiplicacion entre los 2 operandos.
 *
 */
float fMultiplication(float firstNumber, float secondNumber);

/** \brief
 *
 * \param Primer operando.
 * \param Segundo operando.
 * \return El resultado de la resta entre los 2 operandos.
 *
 */
float fSubtraction(float firstNumber, float secondNumber);

/** \brief
 *
 * \param Primer operando.
 * \return El resultado del factorial del primer operando.
 *
 */
float fFactorial(float firstNumber);
